/**
 * This interface defines constants for a simple tic-tac-toe game.
 */
public interface Constants {
    /**
     * Represents a space character on the tic-tac-toe board.
     */
	static final char SPACE_CHAR = ' ';
    /**
     * Represents the letter 'O' used by one of the players in the game.
     */
	static final char LETTER_O = 'O';
    /**
     * Represents the letter 'X' used by the other player in the game.
     */
	static final char LETTER_X = 'X';
}
